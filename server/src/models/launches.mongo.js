// Code here is specific to MongoDB
const mongoose = require('mongoose');

// Defining schema - options are 'validation'
const launchesSchema = new mongoose.Schema({
    flightNumber: {
        type: Number,
        required: true,
    },
    launchDate: {
        type: Date,
        required: true,
    },
    mission: {
        type: String,
        required: true,
    },
    rocket: {
        type: String,
        required: true,
    },
    target: {
        type: String,
    },
    customers: [ String ],
    upcoming: {
        type: Boolean,
        required: true,
    },
    success: {
        type: Boolean,
        required: true,
        default: true,
    },
});

// Connects launchesSchema with the "launches" collection. Compiling the model. 
module.exports = mongoose.model('Launch', launchesSchema); 